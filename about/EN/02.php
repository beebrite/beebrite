<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('9');">What is the BBi?</span>
<div id="aboutitem_9" style="display:none">
BBi (Beebrite index) is a unity scientifically designed by Beebrite for measuring your brain cognitive abilities respect to your personal progress and other users around the world on Beebrite. The user with the highest BBi worldwide, will be the smartest on Beebrite.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('10');">How is measure the reaction Speed?</span>
<div id="aboutitem_10" style="display:none">
The Reaction Speed is measured in milliseconds (ms) and indicate how fast is your brain on Beebrite. The lower the number of the Reaction Speed, it means that your brain is quicker to react to stimuli than other users.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('11');">How do I level in Beebrite?</span>
<div id="aboutitem_11" style="display:none">
It depends on your activity on Beebrite. From what you play, what points you get, your BBi and what daily sessions and trainings complete.
<br /><br />

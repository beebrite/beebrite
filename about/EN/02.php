<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('19');">How to change your username</span>
<div id="aboutitem_19" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and My Profile. Type a new username and then click Save at the bottom of the page.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('20');">How to change your profile picture</span>
<div id="aboutitem_20" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and My Profile. Next, click Change Image. Click the Choose File button to select the image file you want.
<br /><br />
You will see a thumbnail preview of your new image. Click Save at the bottom of the page.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('21');">How to add/remove Beebrite from your Facebook Timeline</span>
<div id="aboutitem_21" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and My Profile. Next, you can add/remove Beebrite to your Facebook Timeline by clicking the slider. ON will add your Stickers and BBi to your Facebook Timeline. OFF will remove Beebrite from your Facebook Timeline.
<br /><br />
Click Save at the bottom of the page when you're finished.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('22');">How to prevent your profile from appearing in Google search</span>
<div id="aboutitem_22" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and My Profile. Select the checkbox to have your profile hidden from search indexes. Be sure to click Save at the bottom of the page.
<br /><br />
This change won't take effect immediately because we cannot flush Google's cache, but they update their information regularly and your profile will soon be fully hidden.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('23');">How to change your preferences for email notifications</span>
<div id="aboutitem_23" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and Notifications. Next, click Change Email Settings. On this page, you can choose which emails you'd like to receive and how frequently. Click Save at the bottom of the page when you're finished.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('24');">How to deactivate your account</span>
<div id="aboutitem_24" style="display:none">
When you're signed into Beebrite, click your username at the top-right toolbar, then click Settings and My Profile. Next, click Delete Account. This will delete all your Stickers, stats and all your data so be careful!

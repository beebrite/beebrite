<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('12');">What are?</span>
<div id="aboutitem_12" style="display:none">
The Stickers are the achievements on Beebrite. There are Stickers for games, for trainings, to get followers, add friends and more. There are two different types of Stickers: Collectibles Stickers &amp; Peel Stickers.
<br /><br />
Collectibles Stickers: Unlock an achievement, and are automatically in your album of Stickers. Nobody can peel it off!
<br /><br />
Peel-Off Stickers: Only one user can have it on Beebrite. To get them, you have to surpass a previous record reached by someone else. If you can surpass that record, that sticker will stay on your album ... but don´t relax, someone can peel it off if surpass your record!
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('13');">How can I get a Sticker?</span>
<div id="aboutitem_13" style="display:none">
To get an Sticker just have to get the objective indicated by its description. If it´s a Peel-Off Sticker, you will have to surpass the current record of that sticker.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('14');">What are the Peel Stickers?</span>
<div id="aboutitem_14" style="display:none">
Only one user can have it on Beebrite. To get them, you have to surpass a previous record reached by someone else. If you can surpass that record, that sticker will stay on your album ... but don´t relax, someone can peel it off if surpass your record!
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('15');">How can I peel-off a Sticker?</span>
<div id="aboutitem_15" style="display:none">
To peel-off a Peel-Off Sticker you have to surpass the previous record reached by the user who currently has it on his album.
<br /><br />
</div>

<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('16');">How can I comment a Sticker?</span>
<div id="aboutitem_16" style="display:none">
There are two ways to comment on a Sticker:
1.	From the Stickers menu, when you see all the Stickers in thumbnail view, click on Add a comment. Type in your comment and then click the Comment button.
2.	When viewing a Sticker in full-size view, you can type a comment in the text box below the Sticker.

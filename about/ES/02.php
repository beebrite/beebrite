<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('9');">¿Qué es el BBi?</span>
<div id="aboutitem_9" style="display:none">
BBi (Beebrite index) o Coeficiente Beebrite es la unidad científicamente diseñada por  Beebrite que mide tus habilidades cognitivas cerebrales con respecto a tu progreso personal y el resto de usuarios de todo el mundo en Beebrite. El usuario con el BBi más alto de todo el mundo, será el más inteligente de Beebrite.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('10');">¿Cómo se mide la Velocidad de reacción?</span>
<div id="aboutitem_10" style="display:none">
La Velocidad de Reacción se mide en milisegundos (ms) y nos va indicar cómo de rápido es nuestro cerebro en Beebrite. Cuánto más bajo sea el numero de la Velocidad de Reacción, significará que tu cerebro es más rápido en reaccionar ante un estímulo que el de otros usuarios.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('11');">¿Cómo subo de nivel en Beebrite?</span>
<div id="aboutitem_11" style="display:none">
Dependerá de tu actividad en Beebrite. De cuanto juegues, de los puntos que consigas, de tu BBi y de cuantos sesiones y entrenamientos completes.
<br /><br />
</div>



<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('19');">Cómo cambiar tu nombre de usuario</span>
<div id="aboutitem_19" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil. Escribe un nuevo nombre de usuario para tu perfil de Beebrite y haz click en el botón Guardar, al final de la página.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('20');">Cómo cambiar la foto de perfil</span>
<div id="aboutitem_20" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil. Ahora haz click en Cambiar Imagen, elige el archivo de imagen que quieras.
<br /><br />
Cuando se cargue, verás una miniatura de la nueva imagen. Haz click en el botón Guardar, al final de la página.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('21');">Cómo añadir/eliminar Beebrite de biografía de Facebook</span>
<div id="aboutitem_21" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil. A continuación, para añadir o eliminar Beebrite en tu biografía de Facebook, haz clic en el control deslizante. La posición de activado añadirá tus Stickers y tu BBi a la biografía de Facebook y la posición de desactivado eliminará Beebrite de tu biografía.
<br /><br />
Haz clic en Guardar al final de la página cuando hayas terminado.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('22');">Cómo hacer que tu perfil no aparezca en las búsquedas de Google</span>
<div id="aboutitem_22" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil.  Selecciona la casilla para ocultar tu perfil a los motores de búsqueda. Asegúrate de hacer clic en Guardar al final de la página.
<br /><br />
Este cambio no será efectivo de forma automática porque no podemos borrar la caché de Google, aunque ellos actualizan su información de forma regular, por lo que tu perfil estará oculto en poco tiempo.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('23');">Cómo cambiar las preferencias de las notificaciones por correo electrónico</span>
<div id="aboutitem_23" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Notificaciones.  A continuación, haz clic en Modificar configuración de correo electrónico. En esta página, puedes elegir qué correos electrónicos te gustaría recibir y con qué frecuencia. Haz clic en Guardar al final de la página cuando hayas terminado.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('24');">Cómo desactivar tu cuenta</span>
<div id="aboutitem_24" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil. A continuación, haz clic en Eliminar cuenta. Se eliminarán tus Stickers, estadísticas y todos tus datos, así que ten mucho cuidado.

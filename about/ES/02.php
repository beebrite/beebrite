<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('1');">¿Qué es Beebrite?</span>
<div id="aboutitem_1" style="display:none">
En Beebrite diseñamos juegos y herramientas capaces de mejorar el rendimiento y la productividad cerebral de cualquier persona del planeta.
<br /><br />
Construimos y creamos una plataforma social que permite jugar, competir, conectar y compartir experiencias entre millones de usuarios.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('2');">¿Que ciencia hay detrás de Beebrite?</span>
<div id="aboutitem_2" style="display:none">
Beebrite cuenta con un comité científico compuesto por investigadores titulares del Instituto Cajal, centro perteneciente al CSIC (Consejo Superior de Investigaciones Científicas) con el que colabora para el diseño-análisis de juegos y entrenamientos.
<br /><br />
El Instituto Cajal se trata del centro de investigación neurobiológica más antiguo de España. Sus investigadores y profesionales de renombre contribuyen al avance espectacular de la Neurobiología en todo el mundo. El Instituto Cajal se prepara para afrontar los retos del futuro y pone al servicio de la sociedad el Legado Cajal.
<br /><br />
&bull;&nbsp;Gonzalo García de Polavieja: PhD in Physical Sciences (Oxford, UK)
<br /><br />
PhD in Physical Sciences as 'Marie Curie' fellow (Oxford, UK) at the Child Group, postdoc in Neuroscience as 'Welcome Trust' fellow in Mathematical Biology at the Laughlin Lab(Cambridge, UK). I spent the autumn of 2000 at Princeton with William Bialek, summer of 2001 at the  Glanzman lab in LA and the summer of 2002 as a Grass Fellow in Neurobiology at the  Marine Biological Laboratory in Woods Hole.  I returned to Spain as 'Ramon y Cajal' fellow (Theoretical Physics, UAM). Presently, I am a staff scientist at the Cajal Institute (CSIC, Spain).
<br /><br />
&bull;&nbsp;Alfonso Pérez Escudero: Bachelor´s Degree in Physical Sciences
<br /><br />
Graduated in June, 2001. Since September, 2001 to June, 2006 studied Physics in the University of Valladolid. During the summer of 2003 worked at Prof. Casanova's lab (Univ. de Valladolid, Spain) on the setting-up and calibration of a vibrating-tube densitometer. Spent the summer and autumn of 2004 at Caltech (Pasadena, California, USA), working with Dr. Alex Bäcker on computational models to predict selective pressure from single genomes. From September to December, 2004 took the courses corresponding to one term of Physics Mayor at Caltech. Spent the summer of 2005 at Max Planck Institut für Quantenoptik (Garching bei München, Germany), working at Prof. Gerhard Rempe's lab on single-molecule cooling by cavity-enhanced Rayleigh scattering. During 2005/2006 academic year worked with Dr. María del Carmen Martínez García on the development of a computer tool for quantitative analysis of histological sections of corneal epithelium. Since September, 2006 collaborates with Dr. Gonzalo García de Polavieja (Universidad Autónoma de Madrid, and Instituto Cajal, Spain) on Wiring Economy models to explain the anatomy of the nervous system, principles of collective behavior and tracking of muliple animals. Since January, 2007 works with Susana Marcos lab at Instituto de Óptica "Daza de Valdés" (Madrid, Spain), studying the biomechanical properties of cornea. Funding for this work is provided by a FPU-MEC predoctoral fellowship.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('3');">¿En qué consiste el programa de Beebrite?</span>
<div id="aboutitem_3" style="display:none">
Beebrite es un entrenador cognitivo que está construido basado en investigaciones neurológicas y ciencias cognitivas. Puedes entrenar las áreas cognitivas de Memoria, Velocidad de Reacción, Concentración, Cálculo y Lenguaje a través de juegos divertidos que son adecuados y efectivos para personas de todas las edades.
<br /><br />
Además de contar con más de 20 juegos y entrenamientos, Beebrite ofrece una sesión diaria de entrenamiento especialmente diseñada para ti. Puedes jugar a juegos de forma individual, o puedes inscribirte en cualquier entrenamiento que se adecúe al área cognitiva que desees mejorar. Cada sesión diaria está diseñada para ser completada en unos 30 minutos.
<br /><br />
</div>



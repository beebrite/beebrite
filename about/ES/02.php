<span id="areastickers" style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:24px; color:#505050; cursor: default;">Stickers</span>
<br />


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('12');">¿Qué son?</span>
<div id="aboutitem_12" style="display:none">
Las Stickers son logros en Beebrite. Existen Stickers por Juegos, por Entrenamientos, por conseguir seguidores, por agregar amigos y mucho más. Hay dos tipos de Stickers diferentes: Stickers Coleccionables y Stickers Despegables. 
<br /><br />
Stickers Coleccionables: Desbloqueas un logro y automáticamente quedan en tu álbum de Stickers. Nadie te la puede despegar!
<br /><br />
Stickers Despegables: Solo puede tenerla un usuario en Beebrite. Conseguirlas supone haber superado un record anterior que había logrado otro usuario. Si consigues superar dicho record, esa Sticker pasará a tu álbum… pero no te relajes, cualquiera te la puede despegar si te superan!
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('13');">¿Cómo consigo una Sticker?</span>
<div id="aboutitem_13" style="display:none">
Para conseguir una Sticker solo debes alcanzar el objetivo que indica su descripción. En el caso de ser una Sticker Despegable, deberás superar el record del actual dueño de esa Sticker.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('14');">¿Qué son las Stickers Despegables?</span>
<div id="aboutitem_14" style="display:none">
Solo pueden tenerlas un usuario en Beebrite. Conseguirlas supone haber superado un record anterior que había logrado otro usuario. Si consigues superar dicho record, esa Sticker pasará a tu álbum… pero no te relajes, cualquiera te la puede despegar si te superan!
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('15');">¿Cómo despego una Sticker?</span>
<div id="aboutitem_15" style="display:none">
Para despegar una Sticker Despegable tienes que superar el record anterior que había logrado el usuario que la tiene actualmente en su álbum.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('16');">¿Cómo comentar una Sticker?</span>
<div id="aboutitem_16" style="display:none">
Hay dos maneras de comentar una Sticker:
1.	Desde el menú Stickers, cuando veas todas en vista de miniatura, haz clic en Añadir un Comentario. Escribe un comentario y haz clic en el botón Comentar.
2.	Con una Sticker en vista de tamaño completo, puedes escribir un comentario en el cuadro de texto debajo de la Sticker.
<br /><br />
</div>



<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('5');">¿Qué son los entrenamientos?</span>
<div id="aboutitem_5" style="display:none">
Son agrupaciones de juegos. Cada entrenamiento está científicamente diseñado para que al combinar dichos juegos se obtenga una mejora significativa. En ocasiones algunos entrenamientos mezclan juegos de varias áreas cognitivas para obtener resultados combinados. 
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('6');">¿En qué consiste la Sesión Diaria?</span>
<div id="aboutitem_6" style="display:none">
La sesión diaria consiste en un entrenamiento diario equilibrado en todas las áreas cognitivas que contiene una serie de juegos personalizados para ti en función de tus intereses o de tu entrenador personal de Beebrite.
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('7');">¿Puedo completar más de 1 Sesión/día?</span>
<div id="aboutitem_7" style="display:none">
No, la Sesión Diaria está programada y diseñada para solo completar 1 sesión/día
<br /><br />
</div>


<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('8');">¿Cuántos entrenamientos puedo cursar al mismo tiempo?</span>
<div id="aboutitem_8" style="display:none">
Todos los que quieras. Independientemente de la Sesión Diaria que te proporciona un entrenamiento equilibrado diario, puedes cursar cualquiera de los Entrenamientos de Beebrite para potenciar otras habilidades cognitivas.
<br /><br />
</div>



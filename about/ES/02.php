<span style="font-family:'myriad-pro'; font-weight:300; font-style:normal; font-size:20px; color:#505050;" onclick="itemToggle('18');">Cómo cambiar la dirección de correo electrónico</span>
<div id="aboutitem_18" style="display:none">
Cuando inicies sesión en Beebrite, haz click en tu nombre de usuario, en la barra superior derecha, después haz click en Configuración y después en Mi Perfil. Escribe una nueva dirección de correo electrónico para tu cuenta de Beebrite y haz click en el botón Guardar, al final de la página.
